//Switch Statement;

let dayOff = "Saturday";

switch (dayOff) {

    case "Saturday":
        console.log("Today is Saturday. I'm coding late at night because tomorrow is a holiday, and I want to finish my project.");
        break;
    case "Sunday":
        console.log("Today is a Sunday, I'm going to play football.");
        break;
    case "Monday":
        console.log("Today is a Monday, I'm reading a book.");
        break;
    default:
        console.log("Today is not a weekday");
        break;
}