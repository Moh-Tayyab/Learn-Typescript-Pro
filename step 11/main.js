//Addition:
const appleTree = 12;
const orangeTree = 15;
const totalTree = appleTree + orangeTree;
console.log(totalTree);
//Subtraction
const flags = 22;
const tickets = 8;
const resultTotal = flags - tickets;
console.log(resultTotal);
//Multiplication:
const civic = 4;
const audi = 5;
const totalCars = civic * audi;
console.log(totalCars);
//Division:
const redFlowers = 32;
const greenFlowers = 12;
const flowerPercent = redFlowers / greenFlowers;
console.log(flowerPercent);
//Exponentiation:
const a = 3;
const b = 3;
const c = a ** b;
console.log(c);
//Module
const x = 15;
const y = 5;
const z = x % y;
console.log(z);
//Unary Operator:
let p = 2;
let q = 3;
let r = ++p + p++ + q-- + q-- + p + q;
console.log(r);
